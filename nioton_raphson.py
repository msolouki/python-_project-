#---------------دیتا ورودی-----------------------
x0 = int(input('ENTER THE NUMBER:'))
#-------------------حلقه شمارنده-------------------
for x in range(0,3):
#------------------------XN+1------------------------
    x = x + 1
#--------------------------فرمول نیتون رافسون----------
    b = x0 - ((x0**2)-2) / (2*x0)
    x0 = b
#------------------------------چاپ اعداد به دست آمده-------
    print('X%d =''%.3f' %(x,b))
    


